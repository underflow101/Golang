//Data Types (Declaration)

package main

import "fmt"

func main() {
	//Full Declaration
	var a int = 10
	fmt.Println(a)

	//Without Explicit Data type
	var b = 15
	fmt.Println(b)

	//Assignment Statement
	c := 20
	fmt.Println(c)

	//String
	var d string = "Hello, World! 1"
	var e = "Hello, World! 2"
	f := "Hello, World! 3"
	fmt.Println(d, e, f)
}
